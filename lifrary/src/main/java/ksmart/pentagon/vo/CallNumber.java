package ksmart.pentagon.vo;

public class CallNumber {
	
	private String uniUnicode;
	private char uniHangul;
	private String uniNum;
	
	
	public String getUniUnicode() {
		return uniUnicode;
	}
	public void setUniUnicode(String uniUnicode) {
		this.uniUnicode = uniUnicode;
	}
	public char getUniHangul() {
		return uniHangul;
	}
	public void setUniHangul(char uniHangul) {
		this.uniHangul = uniHangul;
	}
	public String getUniNum() {
		return uniNum;
	}
	public void setUniNum(String uniNum) {
		this.uniNum = uniNum;
	}
	
	

}
